def build_prompt(type_code: str) -> tuple[str, str]:
    """构建正负面提示词"""
    group = COLOR_GROUPS.get(type_code, "未知")
    color_hex, color_en = GROUP_COLORS.get(group, ("#808080", "gray"))

    role = ROLE_ARCHETYPES.get(type_code)
    if not role:
        raise ValueError(f"未找到类型 {type_code} 的角色原型")
    title, char_desc = role

    positive = POSITIVE_TEMPLATE.format(
        character_desc=char_desc,
        color_name=group,
        color_hex=color_hex,
        title=title,
        type_code=type_code,
    )
    return positive, NEGATIVE_PROMPT


def call_seedream(positive: str, negative: str, max_retries: int = 3) -> str | None:
    """调用 Seedream API"""
    payload = {
        "model": MODEL,
        "prompt": positive,
        "negative_prompt": negative,
        "size": SIZE,
        "response_format": "url",
        "watermark": False,
    }
    data = json.dumps(payload).encode("utf-8")
    headers = volcengine_headers()
    for attempt in range(max_retries):
        try:
            req = urllib.request.Request(API_URL, data=data, headers=headers, method="POST")
            with urllib.request.urlopen(req, timeout=120) as resp:
                result = json.loads(resp.read().decode("utf-8"))
            if "data" in result and len(result["data"]) > 0:
                return result["data"][0]["url"]
            elif "error" in result:
                print(f"  API 错误: {result['error']}")
                if attempt < max_retries - 1:
                    time.sleep((attempt + 1) * 5)
        except Exception as e:
            print(f"  请求异常 ({attempt + 1}/{max_retries}): {e}")
            if attempt < max_retries - 1:
                time.sleep(5)
    return None


def download_image(url: str, filepath: Path) -> bool:
    try:
        req = urllib.request.Request(url)
        with urllib.request.urlopen(req, timeout=60) as resp:
            filepath.write_bytes(resp.read())
        return True
    except Exception as e:
        print(f"  下载失败: {e}")
        return False


def load_state() -> dict:
    if STATE_FILE.exists():
        return json.loads(STATE_FILE.read_text(encoding="utf-8"))
    return {"completed": [], "failed": []}


def save_state(state: dict):
    STATE_FILE.parent.mkdir(parents=True, exist_ok=True)
    STATE_FILE.write_text(json.dumps(state, ensure_ascii=False, indent=2), encoding="utf-8")


def generate_one(type_code: str):
    """生成单张图片"""
    positive, negative = build_prompt(type_code)
    group = COLOR_GROUPS.get(type_code, "未知")

    print(f"\n生成 {type_code} ({group}组)")
    print(f"正面提示词 ({len(positive)} 字符):")
    print(positive)
    print(f"\n负面提示词: {negative}")

    url = call_seedream(positive, negative)
    if url:
        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
        img_path = OUTPUT_DIR / f"{type_code}.jpg"
        if download_image(url, img_path):
            size_kb = img_path.stat().st_size / 1024
            print(f"\n✅ 生成成功: {img_path} ({size_kb:.0f} KB)")
        else:
            print("❌ 下载失败")
    else:
        print("❌ API 调用失败")


def batch_generate():
    """批量生成全部 65 张"""
    all_types = list(ROLE_ARCHETYPES.keys())
    state = load_state()
    completed = set(state.get("completed", []))
    failed = set(state.get("failed", []))
    pending = [t for t in all_types if t not in completed and t not in failed]

    total = len(pending)
    print(f"\n{'=' * 60}")
    print(f"批量生成 {total}/{len(all_types)} 张（已完成 {len(completed)} 张）")
    print(f"{'=' * 60}\n")

    for i, type_code in enumerate(pending):
        group = COLOR_GROUPS.get(type_code, "")
        print(f"[{i + 1}/{total}] {type_code} ({group})", end=" ", flush=True)

        try:
            positive, negative = build_prompt(type_code)
            url = call_seedream(positive, negative)
            if url:
                img_path = OUTPUT_DIR / f"{type_code}.jpg"
                if download_image(url, img_path):
                    completed.add(type_code)
                    state["completed"] = sorted(completed)
                    save_state(state)
                    print(f"✅ {img_path.stat().st_size / 1024:.0f}KB")
                else:
                    failed.add(type_code)
                    state["failed"] = sorted(failed)
                    save_state(state)
                    print("❌ 下载")
            else:
                failed.add(type_code)
                state["failed"] = sorted(failed)
                save_state(state)
                print("❌ API")
        except Exception as e:
            failed.add(type_code)
            state["failed"] = sorted(failed)
            save_state(state)
            print(f"❌ 异常: {e}")

        if i < total - 1:
            time.sleep(DELAY_BETWEEN)

    print(f"\n{'=' * 60}")
    print(f"成功: {len(completed)}/{len(all_types)}  失败: {len(failed)}")
    print(f"输出: {OUTPUT_DIR}")


