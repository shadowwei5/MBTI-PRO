def main():
    conn = sqlite3.connect(DB)
    cur = conn.cursor()

    # 先查出现有的 type code
    cur.execute('SELECT code FROM PersonalityType ORDER BY code')
    existing = set(r[0] for r in cur.fetchall())

    updated = 0
    missing = []

    for code, overview in OVERVIEWS.items():
        if code not in existing:
            missing.append(code)
            continue
        # 去掉首尾空白
        text = overview.strip()
        if not text:
            continue
        cur.execute('UPDATE PersonalityType SET overview = ? WHERE code = ?', (text, code))
        updated += 1
        print(f'  ✓ {code}')

    conn.commit()
    conn.close()

    print(f'\n更新完成: {updated} 条')
    if missing:
        print(f'数据库中不存在的类型: {missing}')

if __name__ == '__main__':
    main()
