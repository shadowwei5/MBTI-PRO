export function buildTypeContent(code: string): TypeContent {
  const chars = {
    E_I: code[0],
    S_N: code[1],
    T_F: code[2],
    P_J: code[3],
  }

  const ei = eiModules[chars.E_I]
  const sn = snModules[chars.S_N]
  const tf = tfModules[chars.T_F]
  const pj = pjModules[chars.P_J]

  // Use proper name from TYPE_NAMES map, fallback to descriptive label
  const name = TYPE_NAMES[code] ?? `${ei.label} · ${sn.label} · ${tf.label} · ${pj.label}`

  // Assemble overview as a flowing narrative
  const eiLabel = ei.char === 'E' ? '外向' : ei.char === 'A' ? '平衡' : '内向'
  const snLabel = sn.char === 'N' ? '直觉' : sn.char === 'B' ? '均衡' : '实感'
  const tfLabel = tf.char === 'T' ? '思考' : tf.char === 'C' ? '复合' : '情感'
  const pjLabel = pj.char === 'J' ? '判断' : pj.char === 'D' ? '动态' : '感知'

  const isCustom = ['A', 'B', 'C', 'D'].some(c => code.includes(c))
  const customIntro = isCustom
    ? `欢迎来到 MBTI-PRO 的精细化人格世界——你的类型代码 ${code} 在传统 MBTI 的 16 型体系中并不存在，这正是我们突破二分法局限、为你量身打造的独特画像。`
    : `你的类型代码 ${code} 代表了 MBTI-PRO 81 型体系中的一个独特人格画像。请记住——即便同属一个类型，每个人也都是独一无二的个体。`

  const overview = `${customIntro}

${ei.summary}

在认知世界的方式上，${sn.summary}

当需要做决定的时候，${tf.summary}

在生活节奏方面，${pj.summary}

${eiLabel}·${snLabel}·${tfLabel}·${pjLabel}——这四个维度的特质交织在一起，构成了你理解世界、做出选择、与人相处的底层操作系统。它不是标签，而是一幅帮你更清晰看见自己的地图。`

  // Collect strengths from all 4 modules (2 × 4 = 8)
  const strengths = [...ei.strengths, ...sn.strengths, ...tf.strengths, ...pj.strengths]

  // Collect growth areas from all 4 modules (1 × 4 = 4)
  const growthAreas = [ei.growth, sn.growth, tf.growth, pj.growth]

  // Career mapping (uses full type code for 81-type precision)
  const { careers, fields } = getCareerPattern(code, chars)

  // Population & celebrities
  const isTraditional = !['A', 'B', 'C', 'D'].some(c => code.includes(c))
  const { population, celebrities } = getTypeMeta(code, chars)

  // 使用81型独立维度模块，替代通用模板
  const typeModules = getTypeDimensionModules(code)

  return {
    code,
    name,
    isTraditional,
    overview,
    strengths,
    growthAreas,
    careers,
    suitableFields: fields,
    population,
    celebrities,
    eiModule: typeModules.eiModule,
    snModule: typeModules.snModule,
    tfModule: typeModules.tfModule,
    pjModule: typeModules.pjModule,
  }
}

