// For 81-type, compute population estimate and celebrity match deterministically
function getTypeMeta(code: string, chars: Record<string, string>): { population: string; celebrities: string[] } {
  if (!['A', 'B', 'C', 'D'].some(c => code.includes(c))) {
    return {
      population: traditionalPopulation[code] || '约 2-5%',
      celebrities: traditionalCelebrities[code] || [],
    }
  }

  // Map balanced positions deterministically to the dominant side
  // A->E, B->N, C->T, D->J (pick the "right" side for consistency)
  const tradCode = [chars.E_I, chars.S_N, chars.T_F, chars.P_J]
    .map(c => {
      if (c === 'A') return 'E'
      if (c === 'B') return 'N'
      if (c === 'C') return 'T'
      if (c === 'D') return 'J'
      return c
    })
    .join('')

  const derivedCelebrities = (traditionalCelebrities[tradCode] || []).slice(0, 3)

  // Balanced types are rarer — estimate based on how many balanced dimensions
  const hasBalance = ['A', 'B', 'C', 'D'].filter(c => code.includes(c)).length
  const pct = hasBalance >= 2 ? '约 0.3-1.0%' : '约 0.5-1.5%'

  return {
    population: pct,
    celebrities: derivedCelebrities,
  }
}

