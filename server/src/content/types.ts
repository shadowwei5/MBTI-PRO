// For 81-type, compute population estimate and celebrity match
function getTypeMeta(code: string, _chars: Record<string, string>): { population: string; celebrities: string[] } {
  const celebrities = CELEBRITY_MAP[code] || []

  if (celebrities.length > 0) {
    const hasBalance = ['A', 'B', 'C', 'D'].filter(c => code.includes(c)).length
    let population: string
    if (hasBalance === 0) {
      population = traditionalPopulation[code] || '约 2-5%'
    } else if (hasBalance >= 2) {
      population = '约 0.3-1.0%'
    } else {
      population = '约 0.5-1.5%'
    }
    return { population, celebrities }
  }

  return { population: '约 1-3%', celebrities: [] }
}

